from managers import UserManager
from bot import bot
import datetime

from utils import is_admin

logger = logging.getLogger(__name__)

def warn_handler(message: Message):
    if message.chat.type in ['group', 'supergroup']:
        # –ü—Ä–æ–≤–µ—Ä–∫–∞ –ø—Ä–∞–≤ –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–∞
        if not is_admin(message.chat.id, message.from_user.id):
            bot.reply_to(message, "‚ùå –£ –≤–∞—Å –Ω–µ—Ç –ø—Ä–∞–≤ –Ω–∞ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ —ç—Ç–æ–π –∫–æ–º–∞–Ω–¥—ã.")
            return

        # –ü—Ä–æ–≤–µ—Ä–∫–∞ –∞—Ä–≥—É–º–µ–Ω—Ç–æ–≤ –∫–æ–º–∞–Ω–¥—ã
        args = message.text.split(' ', 2)
        if len(args) < 3:
            bot.reply_to(message, "‚ö†Ô∏è –ö–æ–º–∞–Ω–¥–∞ –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è —Å–ª–µ–¥—É—é—â–∏–º –æ–±—Ä–∞–∑–æ–º: /warn @username 'reason'")
            return

        username = args[1].replace("@", "")
        reason = args[2]

        user = UserManager.get_user_by_username(message.chat.id, username)
        if user:
            date = datetime.datetime.now().isoformat()
            UserManager.add_warning(message.chat.id, user.user_id, reason, date)
            updated_user = UserManager.load_user(message.chat.id, user.user_id)
            current_warnings = len(updated_user.warnings)
            bot.reply_to(message, f"‚úÖ –ü—Ä–µ–¥—É–ø—Ä–µ–∂–¥–µ–Ω–∏–µ –¥–ª—è @{username} –¥–æ–±–∞–≤–ª–µ–Ω–æ: {reason}\n‚ö†Ô∏è –¢–µ–∫—É—â–µ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø—Ä–µ–¥—É–ø—Ä–µ–∂–¥–µ–Ω–∏–π: {current_warnings}")
        else:
            bot.reply_to(message, f"‚ö†Ô∏è –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å @{username} –Ω–µ –Ω–∞–π–¥–µ–Ω.")

def unwarn_handler(message: Message):
    if message.chat.type in ['group', 'supergroup']:
        # –ü—Ä–æ–≤–µ—Ä–∫–∞ –ø—Ä–∞–≤ –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–∞
        if not is_admin(message.chat.id, message.from_user.id):
            bot.reply_to(message, "‚ùå –£ –≤–∞—Å –Ω–µ—Ç –ø—Ä–∞–≤ –Ω–∞ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ —ç—Ç–æ–π –∫–æ–º–∞–Ω–¥—ã.")
            return

        # –ü—Ä–æ–≤–µ—Ä–∫–∞ –∞—Ä–≥—É–º–µ–Ω—Ç–æ–≤ –∫–æ–º–∞–Ω–¥—ã
        args = message.text.split(' ')
        if len(args) < 2:
            bot.reply_to(message, "‚ö†Ô∏è –ö–æ–º–∞–Ω–¥–∞ –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è —Å–ª–µ–¥—É—é—â–∏–º –æ–±—Ä–∞–∑–æ–º: /unwarn @username [-all]")
            return

        username = args[1].replace("@", "")
        remove_all = '-all' in args

        user = UserManager.get_user_by_username(message.chat.id, username)
        if user:
            if remove_all:
                # –£–¥–∞–ª–µ–Ω–∏–µ –≤—Å–µ—Ö –ø—Ä–µ–¥—É–ø—Ä–µ–∂–¥–µ–Ω–∏–π
                UserManager.clear_warnings(message.chat.id, user.user_id)
                bot.reply_to(message, f"‚úÖ –í—Å–µ –ø—Ä–µ–¥—É–ø—Ä–µ–∂–¥–µ–Ω–∏—è –¥–ª—è @{username} –±—ã–ª–∏ —Å–Ω—è—Ç—ã.\n‚ö†Ô∏è –¢–µ–∫—É—â–µ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø—Ä–µ–¥—É–ø—Ä–µ–∂–¥–µ–Ω–∏–π: 0")
            else:
                # –£–¥–∞–ª–µ–Ω–∏–µ –ø–æ—Å–ª–µ–¥–Ω–µ–≥–æ –ø—Ä–µ–¥—É–ø—Ä–µ–∂–¥–µ–Ω–∏—è
                if user.warnings:
                    UserManager.remove_last_warning(message.chat.id, user.user_id)
                    updated_user = UserManager.load_user(message.chat.id, user.user_id)
                    current_warnings = len(updated_user.warnings)
                    bot.reply_to(message, f"‚úÖ –ü–æ—Å–ª–µ–¥–Ω–µ–µ –ø—Ä–µ–¥—É–ø—Ä–µ–∂–¥–µ–Ω–∏–µ –¥–ª—è @{username} –±—ã–ª–æ —Å–Ω—è—Ç–æ.\n‚ö†Ô∏è –¢–µ–∫—É—â–µ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø—Ä–µ–¥—É–ø—Ä–µ–∂–¥–µ–Ω–∏–π: {current_warnings}")
                else:
                    bot.reply_to(message, f"‚ö†Ô∏è –£ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è @{username} –Ω–µ—Ç –ø—Ä–µ–¥—É–ø—Ä–µ–∂–¥–µ–Ω–∏–π.")
        else:
            bot.reply_to(message, f"‚ö†Ô∏è –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å @{username} –Ω–µ –Ω–∞–π–¥–µ–Ω.")

